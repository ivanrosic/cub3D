#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*ptr;
	size_t	all;

	all = count * size;
	if (!(ptr = malloc(all)))
		return (NULL);
	ft_bzero(ptr, all);
	return (ptr);
}
