#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*str;
	int		i;
	int		j;

	i = 0;
	if ((s1 == NULL) || (s2 == NULL))
		return (NULL);
	j = (ft_strlen(s1) + ft_strlen(s2) + 1);
	if (!(str = malloc(sizeof(char) * j)))
		return (NULL);
	while (s1[i] != '\0')
	{
		str[i] = s1[i];
		i++;
	}
	j = 0;
	while (s2[j] != '\0')
	{
		str[i] = s2[j];
		i++;
		j++;
	}
	str[i] = '\0';
	return (str);
}
